public class DataTypes {
    public static void main(String[] args) {

        //Integer data types
        byte num1 = 120;
        short num2 = 10000;
        int num3 = 234234234;
        long num4 = 4345364635465L;

        //Floating point data types
        float num5 = 1454.3467f;
        double num6 = 343546.6565d;

        //Boolean data type
        boolean redLightSwitch = false;
        boolean blackLightSwitch = true;

        //Character data type
        char char1 = '%';
        char char2 = 'y';
        char char3 = '0';
        char char4 = '\n'; // \n means new line - prints value in a new line

        //String data type
        String text1 = "This is very good text";

        //Print out variable values
        System.out.println(num1);
        System.out.println(num6);
        System.out.println(blackLightSwitch);
        System.out.println(char4); // new line
        System.out.println(char2);
        System.out.println(text1);


       /* num1 = 125;
        System.out.println(num1);*/
    }
}
